x=5
y=2
#Arithmetic operator
#Sum
print(x+y)
#Substraction
print(x-y)
#Multiply
print(x*y)
#Division
print(x/y)
#Exponent
print(x**y)
#Integer Division
print(x//y)

#Relational Operator
print(x>y)
print(x<y)
